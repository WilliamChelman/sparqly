import { DataFactory } from 'n3';
import { parseRdfString } from 'common';
import { resultToFormatted } from './result-to-formatted';

const { namedNode, literal, quad, defaultGraph } = DataFactory;

describe('resultToFormatted', () => {
  it('emits turtle when every quad sits in the default graph', () => {
    const triples = [
      quad(
        namedNode('http://ex/s'),
        namedNode('http://ex/p'),
        literal('o'),
        defaultGraph(),
      ),
    ];
    const out = resultToFormatted(triples, {}, undefined, { prefixes: {} });
    expect(out.serialization).toBe('turtle');
    expect(out.body).toContain('<http://ex/s>');
    expect(out.body).toContain('<http://ex/p>');
    expect(out.body).toContain('"o"');
  });

  it("emits trig when any quad carries a non-default graph", () => {
    const triples = [
      quad(
        namedNode('http://ex/s'),
        namedNode('http://ex/p'),
        literal('o'),
        namedNode('http://ex/g'),
      ),
    ];
    const out = resultToFormatted(triples, {}, undefined, { prefixes: {} });
    expect(out.serialization).toBe('trig');
    expect(out.body).toContain('<http://ex/g>');
  });

  it('uses response prefixes when context has none', () => {
    const triples = [
      quad(
        namedNode('http://ex/s'),
        namedNode('http://ex/p'),
        namedNode('http://ex/o'),
        defaultGraph(),
      ),
    ];
    const out = resultToFormatted(
      triples,
      { ex: 'http://ex/' },
      undefined,
      { prefixes: {} },
    );
    expect(out.body).toContain('@prefix ex: <http://ex/>');
    expect(out.body).toContain('ex:s ex:p ex:o');
  });

  it('uses context prefixes when response has none', () => {
    const triples = [
      quad(
        namedNode('http://ex/s'),
        namedNode('http://ex/p'),
        namedNode('http://ex/o'),
        defaultGraph(),
      ),
    ];
    const out = resultToFormatted(
      triples,
      {},
      undefined,
      { prefixes: { ex: 'http://ex/' } },
    );
    expect(out.body).toContain('@prefix ex: <http://ex/>');
    expect(out.body).toContain('ex:s ex:p ex:o');
  });

  it('prefers context.base over response base', () => {
    const triples = [
      quad(
        namedNode('http://ctx-base/s'),
        namedNode('http://ctx-base/p'),
        namedNode('http://ctx-base/o'),
        defaultGraph(),
      ),
    ];
    const out = resultToFormatted(
      triples,
      {},
      'http://wire-base/',
      { prefixes: {}, base: 'http://ctx-base/' },
    );
    expect(out.body).toContain('@base <http://ctx-base/>');
    expect(out.body).not.toContain('<http://wire-base/>');
  });

  it('falls back to response base when context.base is undefined', () => {
    const triples = [
      quad(
        namedNode('http://wire-base/s'),
        namedNode('http://wire-base/p'),
        namedNode('http://wire-base/o'),
        defaultGraph(),
      ),
    ];
    const out = resultToFormatted(
      triples,
      {},
      'http://wire-base/',
      { prefixes: {} },
    );
    expect(out.body).toContain('@base <http://wire-base/>');
  });

  it('emits no @base directive when neither base is defined', () => {
    const triples = [
      quad(
        namedNode('http://ex/s'),
        namedNode('http://ex/p'),
        literal('o'),
        defaultGraph(),
      ),
    ];
    const out = resultToFormatted(triples, {}, undefined, { prefixes: {} });
    expect(out.body).not.toContain('@base');
  });

  it('resolves a prefix-name conflict in favour of context', () => {
    const triples = [
      quad(
        namedNode('http://ctx/s'),
        namedNode('http://ctx/p'),
        namedNode('http://ctx/o'),
        defaultGraph(),
      ),
    ];
    const out = resultToFormatted(
      triples,
      { ex: 'http://wire/' },
      undefined,
      { prefixes: { ex: 'http://ctx/' } },
    );
    expect(out.body).toContain('@prefix ex: <http://ctx/>');
    expect(out.body).not.toContain('<http://wire/>');
  });

  // Parity: each tuple (rdf-text, expected-cli-output) was generated by
  // piping rdf-text through `sparqly format` (stdin mode, no overrides).
  // The converter, fed the parsed quads/prefixes/base from the same input
  // with an empty DisplayContext, must produce byte-equal output.
  describe('byte-for-byte parity with sparqly format', () => {
    const cases: { name: string; input: string; expected: string }[] = [
      {
        name: 'simple turtle with prefix',
        input: [
          '@prefix ex: <http://example.org/> .',
          'ex:s1 ex:p1 ex:o1 .',
          'ex:s2 ex:p2 "literal" .',
          '',
        ].join('\n'),
        expected: [
          '@prefix ex: <http://example.org/>.',
          '',
          'ex:s1 ex:p1 ex:o1.',
          '',
          'ex:s2 ex:p2 "literal".',
          '',
        ].join('\n'),
      },
      {
        name: 'trig with one named graph',
        input: [
          '@prefix ex: <http://example.org/> .',
          'ex:g {',
          '  ex:s ex:p ex:o .',
          '}',
          '',
        ].join('\n'),
        expected: [
          '@prefix ex: <http://example.org/>.',
          '',
          'ex:g {',
          'ex:s ex:p ex:o',
          '}',
          '',
        ].join('\n'),
      },
      {
        name: 'turtle with @base directive',
        input: [
          '@base <http://example.org/> .',
          '@prefix ex: <http://example.org/> .',
          '<a> ex:knows <b> .',
          '<b> ex:knows <c> .',
          '',
        ].join('\n'),
        expected: [
          '@base <http://example.org/>.',
          '@prefix ex: <http://example.org/>.',
          '',
          '<a> <knows> <b>.',
          '',
          '<b> <knows> <c>.',
          '',
        ].join('\n'),
      },
    ];

    for (const c of cases) {
      it(c.name, () => {
        const parsed = parseRdfString(c.input);
        const out = resultToFormatted(parsed.quads, parsed.prefixes, parsed.base, {
          prefixes: {},
        });
        // The CLI ensures a trailing newline; the converter returns the
        // formatRdf body verbatim. Normalize the comparison the same way
        // the CLI does (`body.endsWith('\n') ? body : body + '\n'`).
        const cliEquivalent = out.body.endsWith('\n') ? out.body : out.body + '\n';
        expect(cliEquivalent).toBe(c.expected);
      });
    }
  });
});
